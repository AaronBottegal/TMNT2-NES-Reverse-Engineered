    DEX ; X--
    BNE PPU_WAIT_A ; Wait more.
    JSR DISABLE_MAPPER_IRQ+SOUND+WEIRDNESS
    JSR MAPPER_IRQ_DISABLE+RTS
    JSR SET_APU_STATUS+SEQUENCE_F_C0
    LDA #$00
    STA TMP_00[4]
    STA TMP_00+1
    LDY #$00 ; Index
    LDX #$08 ; Loop count.
RAM_CLEAR_LOOP: ; 1F:1DD2, 0x03FDD2
    STA [TMP_00[4]],Y ; Clear.
    INY ; Stream++
    BNE RAM_CLEAR_LOOP ; Loop 256
    INC TMP_00+1 ; Inc high addr.
    CPX TMP_00+1 ; Compare to X.
    BNE RAM_CLEAR_LOOP ; Not done, loop more.
    JSR SOUND_INIT_RTN ; Sound.
    JSR INIT_STREAM+MISC_UNK
    JSR ENABLE_NMI+INIT_UNK
    CLI ; Enable interrupts.
INFINITE_LOOP: ; 1F:1DE7, 0x03FDE7
    INC COUNTER_UNK ; Inc
    CLC
    LDA COUNTER_UNK ; Load
    ADC IRQ_COUNT? ; Add with...
    STA COUNTER_UNK ; Store back.
    JMP INFINITE_LOOP ; Loop forever.
FLAG_TRUE: ; 1F:1DF3, 0x03FDF3
    JSR SET_PPU_ADDR/SCROLL/CTRL.
    JSR MUCH_IRQ_RTN/BANKING/ETC/MORE
    JSR BANKSWITCH_R0/R1
    JSR WRITE_R2-R5_RAM
    JSR SOUND_UNK
    LDX SOUND_FLAG_UNK
    LDA COPY_BANK_CFG_F5,X
    STA MMC3_BANK_CFG
    JMP LEAVE_NMI_RESTORE
NMI_HANDLER: ; 1F:1E0C, 0x03FE0C
    PHA ; Save A.
    TXA
    PHA ; Save X.
    TYA
    PHA ; Save Y.
    LDA PPU_STATUS ; Reset latch.
    LDY FLAG_1C_UNK ; Y from.
    BNE FLAG_TRUE
    INC FLAG_1C_UNK ; Set to true.
    LDA #$00
    STA PPU_OAM_ADDR ; Set addr.
    LDY #$02
    STY OAM_DMA ; Upload sprites from 0x0200.
    JSR DISABLE_PPU_RENDERING
    JSR UPDATE_BUF_0x300_TO_PPU
    JSR PPU_READING_SHENANIGANS_RTN ; I think this is to mod health values?
    LDA PPU_MASK_RAM_COPY?
    LDX DISABLE_RENDERING_X_FRAMES ; X from
    BEQ A_TO_PPU_MASK ; == 0, skip.
    DEC DISABLE_RENDERING_X_FRAMES ; --
    BEQ A_TO_PPU_MASK ; If now 0, enable.
    AND #$E7 ; Disable sprites and BG.
A_TO_PPU_MASK: ; 1F:1E39, 0x03FE39
    STA PPU_MASK ; Store mask.
    JSR SET_PPU_ADDR/SCROLL/CTRL. ; Set PPU stuff.
    JSR MUCH_IRQ_RTN/BANKING/ETC/MORE
    LDA MMC3_MIRRORING_COPY ; Load
    STA MMC3_MIRRORING ; Store.
    LDX #$00 ; Index 0.
LOOP_MOVE_3DATA: ; 1F:1E49, 0x03FE49
    LDA R_**:$00AE,X ; Move unk...
    STA R_**:$0656,X
    LDA R_**:$00B1,X
    STA R_**:$0659,X
    LDA R_**:$00B4,X
    STA R_**:$065C,X
    INX ; X++
    CPX #$02
    BNE LOOP_MOVE_3DATA ; Move all.
    JSR MOVE_ZP_BANK_DATA_60D
    JSR BANK_R2-R5
    JSR BANKSWITCH_R0/R1
    JSR SOUND_UNK
    JSR CTRL_READ_SAFE
    JSR BUF_MGMT_UNK
    JSR SWITCH_STATE_18
    JSR L_1E:0E07
    LDX INDEX_300_UPDATE_BUF ; Load index.
    LDA #$00 ; Load
    STA PPU_UPDATE_BUF[8],X ; Store EOF in buf.
    INX ; X++
    STX INDEX_300_UPDATE_BUF ; Store index back.
    STA FLAG_1C_UNK
LEAVE_NMI_RESTORE: ; 1F:1E81, 0x03FE81
    PLA
    TAY ; Restore Y
    PLA
    TAX ; Restore X.
    PLA ; Restore A.
    RTI ; Leave NMI.
SET_PPU_ADDR/SCROLL/CTRL.: ; 1F:1E87, 0x03FE87
    LDA PPU_STATUS ; Reset latch.
    LDA #$20 ; Set addr.
    STA PPU_ADDR
    LDA #$00
    STA PPU_ADDR
    LDA PPU_STATUS ; Reset latch again.
    LDA PPU_SCROLL_X_COPY ; Set scroll.
    STA PPU_SCROLL
    LDA PPU_SCROLL_Y_COPY
    STA PPU_SCROLL
    LDA PPU_CTRL_RAM_COPY ; Set CTRL.
    STA PPU_CTRL
    RTS
SET_APU_STATUS+SEQUENCE_F_C0: ; 1F:1EA7, 0x03FEA7
    LDA #$0F
    STA APU_STATUS
    LDA #$C0
    STA APU_FSEQUENCE
    RTS
ENABLE_NMI+INIT_UNK: ; 1F:1EB2, 0x03FEB2
    LDA #$A8 ; Load. NMI, LSprites, Sprites table 1.
    STA PPU_CTRL_RAM_COPY ; Store
    STA PPU_CTRL ; Store.
    LDA #$1E
    STA PPU_MASK_RAM_COPY? ; Store unk.
    LDA #$05
    STA DISABLE_RENDERING_X_FRAMES ; Store unk.
    RTS
DISABLE_PPU_RENDERING: ; 1F:1EC2, 0x03FEC2
    LDA PPU_CTRL_RAM_COPY ; Load copy.
    AND #$7F ; Disable NMI.
    STA PPU_CTRL ; Commit.
    LDA #$00
    STA PPU_ADDR
    STA PPU_ADDR ; Reset addr.
    LDA PPU_MASK_RAM_COPY? ; Load...copy?
    AND #$E7 ; Disable rendering both sprites and BG.
    STA PPU_MASK ; Store.
    RTS
CTRL_READ_SAFE: ; 1F:1ED9, 0x03FED9
    LDX #$00 ; X=
    JSR LATCH_READ_CTRL
    LDX #$02 ; Index to store other ctrl bits.
    JSR LATCH_READ_CTRL
    LDA TMP_00[4] ; Load first bits.
    CMP TMP_00+2 ; Compare to 2nd read.
    BNE CTRL_FAIL_NO_NEW_PRESSES ; !=, goto.
    LDA TMP_00+1
    CMP TMP_00+3
    BNE CTRL_FAIL_NO_NEW_PRESSES ; Same idea.
    LDX #$00 ; P1 index.
    JSR CTRL_X_PROCESS ; Process
    INX ; P2 index.
CTRL_X_PROCESS: ; 1F:1EF5, 0x03FEF5
    LDA TMP_00[4],X ; Load CTRL buttons.
    TAY ; To Y
    EOR CTRL_PREV_B[2],X ; Get newly pressed only.
    AND TMP_00[4],X ; Newly pressed only.
    STA CTRL_NEWLY_PRESSED_A[2],X
    STA CTRL_NEWLY_PRESSED_B[2],X
    STY CTRL_PREV_A[2],X
    STY CTRL_PREV_B[2],X
    RTS
CTRL_FAIL_NO_NEW_PRESSES: ; 1F:1F05, 0x03FF05
    LDA #$00
    STA CTRL_NEWLY_PRESSED_A[2]
    STA CTRL_NEWLY_PRESSED_B[2]
    STA CTRL_NEWLY_PRESSED_A+1
    STA CTRL_NEWLY_PRESSED_B+1
    RTS
LATCH_READ_CTRL: ; 1F:1F10, 0x03FF10
    LDY #$01 ; Y=
    STY NES_CTRL1 ; Reset CTRL.
    DEY ; Y=0
    STY NES_CTRL1 ; Complete latch.
    LDY #$08 ; Loop count.
LOOP_READ_CTRL: ; 1F:1F1B, 0x03FF1B
    LDA NES_CTRL1 ; Load ctrl.
    STA CTRL_RAM[2] ; Store to RAM.
    LSR A ; Shift.
    ORA CTRL_RAM[2] ; Or other bits...
    LSR A ; Shift again.
    ROL TMP_00[4],X ; P1 bits.
    LDA NES_CTRL2 ; Load ctrl.
    STA CTRL_RAM+1 ; Store to RAM.
    LSR A ; Shift
    ORA CTRL_RAM+1 ; Gather bits.
    LSR A ; Shift
    ROL TMP_00+1,X ; P2 bits/
    DEY ; Loop--
    BNE LOOP_READ_CTRL
    RTS
